    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        pos=Integer.parseInt(tc_pos.getText());
        if(u.get(pos).getNu().equals(tc_us.getText())){
            u.get(pos).setNomb(this.tc_n.getText());
            u.get(pos).setApel(this.tc_a.getText());
            u.get(pos).setTel(this.tc_t.getText());
            u.get(pos).setCor(this.tc_c.getText());
            u.get(pos).setPlan(this.tc_p.getText());
            u.get(pos).setNu(this.tc_us.getText());
            u.get(pos).setPass(this.tc_p.getText());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

